var btns = document.getElementsByClassName('title_letter');



btns[0].onmouseenter  = function() {
  btns[0].classList.add("jump-animation");
}

btns[0].onmouseleave  = function() {
  if(btns[0].classList.contains("jump-animation")){
    setTimeout(function(){
        btns[0].classList.remove("jump-animation");
    },1000);       
  }
}


